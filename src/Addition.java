public class Addition {
    //Atributos
    private int num1;
    private int num2;

    //Constructor
    public Addition(int num1, int num2) {
        this.num1 = num1;
        this.num2 = num2;
    }

    //Metodos
    public int add(int num1, int num2) {
        return num1 + num2;
    }

}
